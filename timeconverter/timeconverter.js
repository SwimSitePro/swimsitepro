function convertTime() {
    const gender = document.getElementById("gender").value;
    const ageGroup = document.getElementById("ageGroup").value;
    const event = document.getElementById("event").value;
    const minutes = parseInt(document.getElementById("minutes").value, 10) || 0;
    const seconds = parseInt(document.getElementById("seconds").value, 10) || 0;
    const milliseconds = parseInt(document.getElementById("milliseconds").value, 10) || 0;
    const fromType = document.getElementById("fromType").value; 
    const toType = document.getElementById("toType").value; 

    const record = worldRecords[gender]?.[ageGroup]?.[event];

    if (!record) {
        document.getElementById("result").textContent = "Record data for this age group and event is unavailable.";
        return;
    }

    if (seconds >= 60 || milliseconds >= 100) {
        document.getElementById("result").textContent = "Please enter valid time values.";
        return;
    }

    const totalTimeInSeconds = minutes * 60 + seconds + milliseconds / 100;

    if (!record[fromType] || !record[toType]) {
        document.getElementById("result").textContent = "Conversion not supported for this event.";
        return;
    }

    const convertedTime = record[toType] * (totalTimeInSeconds / record[fromType]);

    const convertedMinutes = Math.floor(convertedTime / 60);
    const remainingSeconds = convertedTime % 60;
    const paddedSeconds = remainingSeconds.toFixed(2).padStart(5, '0');

    document.getElementById("result").textContent = `Converted Time: ${convertedMinutes}:${paddedSeconds}`;
}